def calculate_materials():
    totals = {}

    # Loop through tracker entries
    for tracker_type, entry in tracker_entries.items():
        try:
            count = int(entry.get())
        except ValueError:
            count = 0

        if count > 0:
            for item, qty in trackers[tracker_type].items():
                if isinstance(qty, dict):  # Handle PIERS sub-dictionary
                    for sub_item, sub_qty in qty.items():
                        totals[sub_item] = totals.get(sub_item, 0) + sub_qty * count
                else:
                    totals[item] = totals.get(item, 0) + qty * count

    
    show_results(totals)


def show_results(totals):
    result_window = tk.Toplevel(root)
    result_window.title("Total Materials Required")

    tree = ttk.Treeview(result_window, columns=("Item Number", "Item Name", "Quantity", "Image"), show="headings")
    tree.heading("Item Number", text="Item Number")
    tree.heading("Item Name", text="Item Name")
    tree.heading("Quantity", text="Quantity")
    tree.heading("Image", text="Image")

    tree.column("Item Number", width=120)
    tree.column("Item Name", width=500)
    tree.column("Quantity", width=100)
    tree.column("Image", width=100)

    for item, qty in totals.items():
        name = item_names.get(item, "Unknown Item")
        has_image = "Yes" if int(item) in item_images  else "No"
        tree.insert("", "end", values=(item, name, qty, has_image))

    tree.pack(fill="both", expand=True)

    def open_image(event):
        selected = tree.selection()
        if not selected:
            return
        item_id = tree.item(selected[0])["values"][0]
        if item_id in item_images:
            webbrowser.open(item_images[item_id])
        else:
            messagebox.showinfo("No Image", f"No image available for Item {item_id}")

    tree.bind("<Double-1>", open_image)


